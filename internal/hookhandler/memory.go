func buildFailuresSection(_ *store.Store, _ string) string {
	return ""
}

// buildDecisionsSection generates the "Important Design Decisions" section.
func buildDecisionsSection(st *store.Store, cwd string) string {
	projectName := filepath.Base(cwd)
	decisions, err := st.GetDecisions("", projectName, 5)
	if err != nil || len(decisions) == 0 {
		return ""
	}

	var b strings.Builder
	b.WriteString("## Important Design Decisions\n")

	for _, d := range decisions {
		text := d.DecisionText
		if len([]rune(text)) > 120 {
			text = string([]rune(text)[:120])
		}

		if d.Topic != "" {
			topic := d.Topic
			if len([]rune(topic)) > 60 {
				topic = string([]rune(topic)[:60])
			}
			fmt.Fprintf(&b, "- %s (%s)\n", text, topic)
		} else {
			fmt.Fprintf(&b, "- %s\n", text)
		}
	}

	return b.String()
}

// buildCoChangeSection generates the "File Co-change Patterns" section.
func buildCoChangeSection(st *store.Store) string {
	pairs, err := st.TopCoChangePairs(5)
	if err != nil || len(pairs) == 0 {
		return ""
	}

	var b strings.Builder
	b.WriteString("## File Co-change Patterns\n")

	for _, cc := range pairs {
		a := shortenPath(cc.FileA)
		bPath := shortenPath(cc.FileB)
		fmt.Fprintf(&b, "- %s <-> %s (%d sessions)\n", a, bPath, cc.SessionCount)
	}

	return b.String()
}

// buildWorkStyleSection generates the "Work Style" section.
func buildWorkStyleSection(st *store.Store) string {
	cluster := st.UserCluster()

	tf, tfCount, _ := st.GetUserProfile("test_frequency")
	rw, rwCount, _ := st.GetUserProfile("read_write_ratio")
	tpb, tpbCount, _ := st.GetUserProfile("tools_per_burst")
	cf, cfCount, _ := st.GetUserProfile("compact_frequency")

	// Need at least some data to be useful.
	if tfCount == 0 && rwCount == 0 {
		return ""
	}

	var b strings.Builder
	b.WriteString("## Work Style\n")
	fmt.Fprintf(&b, "- Cluster: %s", cluster)
	if tfCount > 0 {
		fmt.Fprintf(&b, " | Test frequency: %.2f", tf)
	}
	if rwCount > 0 {
		fmt.Fprintf(&b, " | R/W ratio: %.1f", rw)
	}
	b.WriteString("\n")
	if tpbCount > 0 || cfCount > 0 {
		b.WriteString("- Session profile:")
		if tpbCount > 0 {
			fmt.Fprintf(&b, " avg tools/burst: %.0f", tpb)
		}
		if cfCount > 0 {
			fmt.Fprintf(&b, " | compact frequency: %.2f", cf)
		}
		b.WriteString("\n")
	}

	return b.String()
}

