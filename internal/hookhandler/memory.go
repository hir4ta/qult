// shortenPath strips common prefixes to make file paths more readable.
// Converts absolute paths to relative-looking paths.
func shortenPath(p string) string {
	// Strip leading slashes and common home dir prefix for brevity.
	if idx := strings.Index(p, "/internal/"); idx >= 0 {
		return p[idx+1:]
	}
	if idx := strings.Index(p, "/src/"); idx >= 0 {
		return p[idx+1:]
	}
	if idx := strings.Index(p, "/pkg/"); idx >= 0 {
		return p[idx+1:]
	}
	// Use just the base filename and parent dir for readability.
	dir := filepath.Dir(p)
	base := filepath.Base(p)
	parent := filepath.Base(dir)
	if parent != "." && parent != "/" {
		return parent + "/" + base
	}
	return base
}

// homeDir returns the user's home directory, falling back to ".".
func homeDir() string {
	home, err := os.UserHomeDir()
	if err != nil {
		return "."
	}
	return home
}
