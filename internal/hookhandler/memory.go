// persistSessionMemory writes learned knowledge to Claude Code's memory directory
// so it auto-loads every session. Called at the end of persistSessionData().
// All errors go to stderr; this function never blocks SessionEnd.
func persistSessionMemory(sessionID, cwd string) {
	if cwd == "" {
		return
	}

	st, err := store.OpenDefaultCached()
	if err != nil {
		fmt.Fprintf(os.Stderr, "memory: open store: %v\n", err)
		return
	}

	// Skip if not enough sessions to produce useful knowledge.
	stats, err := st.GetProjectSessionStats(cwd)
	if err != nil || stats.TotalSessions < 3 {
		return
	}

	projectKey := deriveProjectKey(cwd)
	memDir := filepath.Join(homeDir(), ".claude", "projects", projectKey, "memory")
	if err := os.MkdirAll(memDir, 0o755); err != nil {
		fmt.Fprintf(os.Stderr, "memory: mkdir %s: %v\n", memDir, err)
		return
	}

	content := generateMemoryContent(st, cwd, stats.TotalSessions)
	if content == "" {
		return
	}

	// Atomic write: write to .tmp, then rename.
	target := filepath.Join(memDir, "MEMORY.md")
	tmp := target + ".tmp"
	if err := os.WriteFile(tmp, []byte(content), 0o644); err != nil {
		fmt.Fprintf(os.Stderr, "memory: write tmp: %v\n", err)
		return
	}
	if err := os.Rename(tmp, target); err != nil {
		fmt.Fprintf(os.Stderr, "memory: rename: %v\n", err)
		_ = os.Remove(tmp)
	}
}

// deriveProjectKey converts an absolute path to Claude Code's project key format.
// Example: /Users/user/Projects/claude-alfred -> -Users-user-Projects-claude-alfred
func deriveProjectKey(cwd string) string {
	return strings.ReplaceAll(cwd, "/", "-")
}

// generateMemoryContent builds the MEMORY.md content from store data.
// Returns empty string if there's nothing meaningful to write.
// Keeps output under 200 lines (Claude Code's memory file truncation limit).
func generateMemoryContent(st *store.Store, cwd string, totalSessions int) string {
	var b strings.Builder

	// Header.
	b.WriteString("# claude-alfred: Project Learning Notes\n\n")
	b.WriteString("> Auto-generated by claude-alfred. Do not edit manually.\n")
	fmt.Fprintf(&b, "> Last updated: %s | Sessions: %d\n", time.Now().UTC().Format(time.RFC3339), totalSessions)

	sections := 0

	// Common failures and solutions.
	if s := buildFailuresSection(st, cwd); s != "" {
		b.WriteString("\n")
		b.WriteString(s)
		sections++
	}

	// Important design decisions.
	if s := buildDecisionsSection(st, cwd); s != "" {
		b.WriteString("\n")
		b.WriteString(s)
		sections++
	}

	// File co-change patterns.
	if s := buildCoChangeSection(st); s != "" {
		b.WriteString("\n")
		b.WriteString(s)
		sections++
	}

	// Work style.
	if s := buildWorkStyleSection(st); s != "" {
		b.WriteString("\n")
		b.WriteString(s)
		sections++
	}

	// Suggestion accuracy.
	if s := buildAccuracySection(st); s != "" {
		b.WriteString("\n")
		b.WriteString(s)
		sections++
	}

	if sections == 0 {
		return ""
	}

	// Enforce 200-line limit.
	result := b.String()
	lines := strings.Split(result, "\n")
	if len(lines) > 200 {
		lines = lines[:200]
		result = strings.Join(lines, "\n")
	}

	return result
}

